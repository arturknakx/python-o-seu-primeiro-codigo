salario_func1 = 5000
salario_func2 = 7000

if salario_func1 == salario_func2:
    print('Os salários dos funcionários são iguais.')
elif salario_func1 != salario_func2:
    print('O salário dos funcionários são diferentes.')
elif salario_func1 > salario_func2:
    print('Funcionário 1 ganha mais do que o funcionário 2.')
elif salario_func1 >= salario_func2:
    print('Funcionário 1 ganha mais ou igual ao funcionário 2.')
elif salario_func1 < salario_func2:
    print('Funcionário 1 ganha menos do que o funcionário 2.')
elif salario_func1 >= salario_func2:
    print('Funcionário 1 ganha menos ou igual ao funcionário 2.')
